WHAT IS SQL ?
-------------
IT IS A QUERY LANGUAGE,
USED TO ACCESS AND MANIPULATE DATA,
PERFORMING CRUD OPERATIONS.

==============================================================
--SQL STATEMENTS 
-----------------------------------------
DDL : DATA DEFINATION LANGUAGE  
        USED TO DEAL WITH OBJECTS IN DB
    CREATE
    ALTER
    DROP
    TRUNCATE
    RENAME
    COMMENT
    
DML : DATA MANIPULATION LANGUAGE
        USED TO MODIFY DATA IN TABLE
    SELECT 
    INSERT
    UPDATE
    DELETE
    
    
DCL : DATA CONTROL LANGUAGE
     USED TO GIVE OR REMOVE PERMISSION FROM USER.
    GRANT
    REVOKE

TCL : TRANSACTION CONTROL LANGUAGE
    USED TO CONTROL TRANSACTION.    
    COMMIT
    ROLLBACK
    SAVEPOINT
    

SQL MINDMAP:
===============

DATA : RAW DATA OR SOMNE INFORMATION.

INFORMATION : RELATED DATA OR MEANINGFUL DATA.

DATABASE  : COLLECTION OF RELATED DATA


OBJECTS : IT IS STRUCTURE IN WHICH USED TO STORE DATA IN DB.
LIST OF OBJECTS :                  
                    TABLE  :DATA STORED IN ROW AND COLUMN FORMAT.
                    VIEW   : LOGICALLY REPRESENT DATA ,ADDING 2 OR MORE TABLES.
                    INDEX  : USED TO IMPROVE PERFORMANCE OF QUERY.
                    CONSTRAINTS : USED TO RESTRICT DATA TO INFORCE RULES IN TABLE.
                    SYNONYM  : IT IS ALTERNATIVE NAME TO OBJECTS.
                    SEQUENCE : USED TO GENERATE PRIMARY KEY VALUES.
                    TRIGGERS : INVOKES AUTOMATICALLY WHENEVER SPECIFIED EVENT OCCURS.
